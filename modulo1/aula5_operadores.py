"""
+,-,*,
/ = divisao
// = divisão inteiro
** = potencia
% = resto da divisao ,
() = ordem

"""

print(10 * 10)  # multiplicação
print(10 + 10)  # Adição
print(10 - 5)  # subtração
print(10 / 2)  # Divisao

print(10 // 2)  # divisao com resultado inteiro
print(5 ** 2)  # potencia
print(25 % 3)  # resto
print(2+2*2)
print((2+2)*2)
